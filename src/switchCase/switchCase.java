package switchCase;

public class switchCase {
public static void main(String[] args) {
	
	switch(24)
	{
	case 1: System.out.println("Num is Ten");
	break;
	case 2: System.out.println("Num is two");
	break;
	case 50: System.out.println("Num is 50");
	break;
	default: System.out.println("Different number");
	}
}
}
